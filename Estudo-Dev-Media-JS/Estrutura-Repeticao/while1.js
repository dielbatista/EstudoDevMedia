/**
 * A estrutura de repetição while executa um bloco de código enquanto a condição for verdadeira
 */

let contador = 0 //definido um contador com valor 0

while(contador <5 ){ //verifica se o contador é < que 5
    contador++  //incrementa o contador até o valor chegar a 5
    console.log(contador) //imprime o resultado na tela
}
